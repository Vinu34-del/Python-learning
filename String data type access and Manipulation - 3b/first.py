# Concatination
first_name = "Vinay"
last_name = "Devadiga"
full_name = first_name + " " + last_name
print(full_name)

# Repeatation
message = "Hello " * 5
print(message)

# String Methods
str = "Vinay"
print(str.upper())
print(str.lower())
print(str.capitalize())
print(str.strip())
print(str.replace("Vinay", "vinay"))
print(len(str))

# String index
text = "Python"
print(text[2])  # t
print(text[2:6])  # thon
print(text[0:])
print(text[:6])
print(text[-1])
print(text[::2])  # skip element

# Escape sequence
word = "Vinay is a \n good boy"  # next line
print(word)

word_2 = "Vinay is a \t good boy"  # tab
print(word_2)
